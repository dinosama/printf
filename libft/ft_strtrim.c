#include "libft.h"

static int		setsins(char c, char const *set)
{
	size_t	i;

	i = 0;
	while (set[i])
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char			*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	n;

	if (!s1)
		return (0);
	i = 0;
	n = ft_strlen(s1);
	while (s1[i] && setsins(s1[i], set) == 1)
		i++;
	while (setsins(s1[n - 1], set) == 1 && n > 0 && n > i)
		n--;
	return (ft_substr(s1, i, (n - i)));
}
