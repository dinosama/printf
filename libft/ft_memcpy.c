#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	int				i;
	unsigned char	*str;
	unsigned char	*str2;

	i = 0;
	str = (unsigned char*)dest;
	str2 = (unsigned char*)src;
	if (!str && !str2)
		return (NULL);
	while ((size_t)i < n)
	{
		str[i] = str2[i];
		i++;
	}
	return (dest);
}
