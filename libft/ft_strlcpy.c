#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;

	if (!dest || !src)
		return (0);
	if (size == 0)
		return (ft_strlen((char*)src));
	i = 0;
	while (i < size - 1 && src[i])
	{
		((char*)dest)[i] = ((char*)src)[i];
		i++;
	}
	((char*)dest)[i] = '\0';
	return (ft_strlen(((char*)src)));
}
